<script setup>
import { ref, onMounted, computed, nextTick } from 'vue'
import { gsap } from 'gsap'
import { RouterLink } from 'vue-router'
import footerBg from '../../../assets/footer/backgroundfooter.png' // üëà your image here

const isMemberOpen = ref(false)
const isAboutOpen = ref(false)

const year = computed(() => new Date().getFullYear())

const animateDropdownOpen = async (selector) => {
    await nextTick()
    const el = document.querySelector(selector)
    if (!el) return

    gsap.fromTo(
        el,
        { height: 0, opacity: 0 },
        {
            height: el.scrollHeight,
            opacity: 1,
            duration: 0.35,
            ease: 'power2.out',
            onComplete: () => {
                gsap.set(el, { height: 'auto' })
            }
        }
    )
}

const toggleDropdown = (section) => {
    if (section === 'member') {
        const opening = !isMemberOpen.value
        isMemberOpen.value = opening
        if (opening) {
            animateDropdownOpen('.dropdown-member')
        }
    }

    if (section === 'about') {
        const opening = !isAboutOpen.value
        isAboutOpen.value = opening
        if (opening) {
            animateDropdownOpen('.dropdown-about')
        }
    }
}

onMounted(() => {
    const tl = gsap.timeline({ defaults: { ease: 'power3.out' } })

    tl.from('.footer-wrapper', {
        opacity: 0,
        y: 40,
        duration: 0.8
    })
        .from(
            '.footer-top',
            {
                opacity: 0,
                y: 30,
                duration: 0.7
            },
            '-=0.3'
        )
        .from(
            '.footer-brand',
            {
                opacity: 0,
                x: -20,
                duration: 0.6
            },
            '-=0.4'
        )
        .from(
            '.footer-contact .contact-item',
            {
                opacity: 0,
                x: 20,
                duration: 0.45,
                stagger: 0.1
            },
            '-=0.4'
        )
        .from(
            '.footer-main-inner',
            {
                opacity: 0,
                y: 30,
                duration: 0.7
            },
            '-=0.25'
        )
        .from(
            '.footer-column',
            {
                opacity: 0,
                y: 20,
                duration: 0.6,
                stagger: 0.15
            },
            '-=0.3'
        )
        .from(
            '.footer-bottom',
            {
                opacity: 0,
                y: 10,
                duration: 0.5
            },
            '-=0.4'
        )

    // Floating logo animation
    gsap.to('.footer-logo', {
        y: -6,
        duration: 2.4,
        ease: 'sine.inOut',
        repeat: -1,
        yoyo: true
    })

